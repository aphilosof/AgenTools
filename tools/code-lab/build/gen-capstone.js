/* gen-capstone.js — generate src/js/capstone.js from src/capstone/*.md.

   The capstone guides are long-form Markdown. A browser file can't read them at
   runtime, so we embed each one as a JSON.stringify'd string (code fences and
   backticks survive intact — never hand-embed with template literals). Run:

       node build/gen-capstone.js

   Re-run whenever the .md guides change. Output is committed like reference.js. */

const fs = require("fs");
const path = require("path");

const CAP = path.join(__dirname, "..", "src", "capstone");
const OUT = path.join(__dirname, "..", "src", "js", "capstone.js");

// Ordered: setup first (order 0), then the three project briefs.
const GUIDES = [
  { id: "setup",          order: 0, file: "CAPSTONE_00_SETUP.md",        title: "Set up Python on your Mac" },
  { id: "beat-machine",   order: 1, file: "CAPSTONE_BEAT_MACHINE.md",    title: "The Beat Machine" },
  { id: "data-dashboard", order: 2, file: "CAPSTONE_DATA_DASHBOARD.md",  title: "The Data Dashboard" },
  { id: "data-analyzer",  order: 3, file: "CAPSTONE_DATA_ANALYZER.md",   title: "The Data Analyzer" },
];

const items = GUIDES.map(function (g) {
  return {
    id: g.id,
    title: g.title,
    order: g.order,
    body: fs.readFileSync(path.join(CAP, g.file), "utf8"),
  };
});

const banner =
  "/* capstone.js — GENERATED by build/gen-capstone.js from src/capstone/*.md.\n" +
  "   Do not edit by hand; re-run: node build/gen-capstone.js\n" +
  "   window.CODELAB.capstone = ordered [{ id, title, order, body }]; body is the\n" +
  "   guide's raw Markdown, JSON-encoded so code fences and backticks survive. */\n";

const out =
  banner +
  "(function () {\n" +
  "  var CL = (window.CODELAB = window.CODELAB || {});\n" +
  "  CL.capstone = " + JSON.stringify(items, null, 2).replace(/\n/g, "\n  ") + ";\n" +
  "})();\n";

fs.writeFileSync(OUT, out);
console.log("wrote " + path.relative(process.cwd(), OUT) + " (" + items.length + " guides)");
